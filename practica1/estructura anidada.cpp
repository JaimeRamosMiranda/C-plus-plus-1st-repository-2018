/*
"Estructura selectiva multiple":
Se usa en situaciones, donde hay varias alternativas de seleccion, esto en funcion
del valor que tome un selector.
El selector puede ser una variable o un valor, sin embargo el valor que representa
debe ser entero o caracter, de ninguna manera puede ser real ni cadena
*/                                                                    

/*
PSEUDOCODIGO
valor (selector)
seguir el bloque, segun el valor
	1: proceso
	2:proceso
	...
	n: proceso
segun

C++
switch(selector)
	{
	case 1: proceso; break;
	case 2: proceso; break;
	...
	case n: proceso; break;
	default: cout
	} 
*/

